public class Clase_IX {
    private String pressagmin;
    private String pressagmax;
    private String numpuls;
    private String ritmo;
    private String temperatura;

    public String getPressagmin() {
        return pressagmin;
    }

    public void setPressagmin(String pressagmin) {
        this.pressagmin = pressagmin;
    }

    public String getPressagmax() {
        return pressagmax;
    }

    public void setPressagmax(String pressagmax) {
        this.pressagmax = pressagmax;
    }

    public String getNumpuls() {
        return numpuls;
    }

    public void setNumpuls(String numpuls) {
        this.numpuls = numpuls;
    }

    public String getRitmo() {
        return ritmo;
    }

    public void setRitmo(String ritmo) {
        this.ritmo = ritmo;
    }

    public String getTemperatura() {
        return temperatura;
    }

    public void setTemperatura(String temperatura) {
        this.temperatura = temperatura;
    }
}
